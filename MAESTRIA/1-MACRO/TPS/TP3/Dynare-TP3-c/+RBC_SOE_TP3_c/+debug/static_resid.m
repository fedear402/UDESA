function [lhs, rhs] = static_resid(y, x, params)
T = NaN(9, 1);
lhs = NaN(11, 1);
rhs = NaN(11, 1);
T(1) = exp(y(5))*(1-params(5)-params(10))*exp(y(4))^((-params(5))-params(10));
T(2) = exp(y(3))^params(5);
T(3) = exp(y(7))^params(10);
T(4) = exp(y(2))-exp(y(4))^params(7)/params(7);
T(5) = T(4)^(-params(3));
T(6) = exp(y(5))*params(5)*exp(y(3))^(params(5)-1);
T(7) = T(3)*T(6);
T(8) = exp(y(4))^(1-params(5)-params(10));
T(9) = exp(y(7))^(params(10)-1);
lhs(1) = exp(y(1))+y(9);
rhs(1) = exp(y(2))+exp(y(3))-exp(y(3))*(1-params(4))+exp(y(6))*exp(y(7))+y(9)*(1+params(14)+params(2)*(exp(y(9)-params(15))-1));
lhs(2) = exp(y(4))^(params(7)-1);
rhs(2) = T(1)*T(2)*T(3);
lhs(3) = T(5);
rhs(3) = T(5)*(1+params(14)+params(2)*(exp(y(9)-params(15))-1))*params(1);
lhs(4) = 1;
rhs(4) = params(1)*(1-params(4)+T(7)*T(8));
lhs(5) = T(8)*T(2)*exp(y(5))*params(10)*T(9);
rhs(5) = exp(y(6));
lhs(6) = exp(y(1));
rhs(6) = T(8)*T(3)*exp(y(5))*T(2);
lhs(7) = exp(y(3));
rhs(7) = exp(y(3))*(1-params(4))+exp(y(8));
lhs(8) = y(11);
rhs(8) = 0;
lhs(9) = y(10);
rhs(9) = (exp(y(1))-exp(y(2))-exp(y(8)))/exp(y(1));
lhs(10) = y(5);
rhs(10) = y(5)*params(6)+params(9)*x(1);
lhs(11) = y(6);
rhs(11) = y(6)*params(12)+params(13)*x(2);
end
